#import "UIColor+Addition.h"

@implementation UIColor (Addition)

+ (UIColor*)fashTagBackgroundColor
{
  return [UIColor colorWithRed:0.92 green:0.93 blue:0.93 alpha:1.0];
}

+ (UIColor*)fashTagPurpleColor
{
  return [UIColor colorWithRed:0.41 green:0.23 blue:0.66 alpha:1.0];
}

+ (UIColor*)fashTagDarkTextColor
{
  return [UIColor colorWithRed:0.282 green:0.282 blue:0.282 alpha:1.0];
}

+ (UIColor*)fashTagGrayTextColor
{
  return [UIColor grayColor];
}

@end
